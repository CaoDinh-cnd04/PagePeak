/** Danh sách categories duy nhất từ static templates */
export function getStaticCategories(): string[] {
  return [...new Set(STATIC_TEMPLATES.map((t) => t.category))].sort();
}

/** Lọc và sort static templates (dùng trong DashboardTemplates khi không có API) */
export function filterStaticTemplates(params?: {
  category?: string;
  search?: string;
  designType?: string;
  featured?: boolean;
}): StaticTemplateItem[] {
  let list = [...STATIC_TEMPLATES];
  if (params?.category) list = list.filter((t) => t.category === params.category);
  if (params?.search) {
    const q = params.search.toLowerCase();
    list = list.filter(
      (t) =>
        t.name.toLowerCase().includes(q) ||
        (t.description ?? "").toLowerCase().includes(q) ||
        t.category.toLowerCase().includes(q),
    );
  }
  if (params?.designType) list = list.filter((t) => t.designType === params.designType);
  if (params?.featured) list = list.filter((t) => t.isFeatured);
  return list.sort((a, b) => b.usageCount - a.usageCount);
}
