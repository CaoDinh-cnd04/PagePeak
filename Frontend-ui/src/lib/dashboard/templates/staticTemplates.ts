import type { TemplateItem } from "@/lib/shared/api";

function makeIframeJsonContent(iframeSrc: string): string {
  return JSON.stringify({
    sections: [
      {
        id: 1,
        pageId: 0,
        order: 1,
        height: 8000,
        visible: true,
        isLocked: false,
        name: "Template",
        backgroundColor: null,
        backgroundImageUrl: null,
        elements: [
          {
            id: 1,
            sectionId: 1,
            type: "html-code",
            order: 1,
            x: 0,
            y: 0,
            width: 960,
            height: 8000,
            zIndex: 1,
            rotation: 0,
            opacity: 1,
            isLocked: false,
            isHidden: false,
            content: JSON.stringify({ subType: "iframe", iframeSrc }),
            styles: { fullScreen: true },
          },
        ],
      },
    ],
  });
}

